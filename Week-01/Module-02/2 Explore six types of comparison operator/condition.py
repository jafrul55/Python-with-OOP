# 6 types of comparison
# < check wheather the left side of the symbol is smaller than the right side
""" print(12<35)
print(42 < 35) """
# > (check bigger or not)
""" print(12>45)
print(89>12) """

# >= To check whether bigger or equal
""" print(12>=12) """
# <= To check whether smaller or equal
""" print(42 <= 35) """

