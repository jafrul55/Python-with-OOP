class Calculator:
    def add(self,a,b):
        return a+b
    
    def subtract(self,a,b):
        return a-b
    
    def multiply(self,a,b):
        return a * b
    
    def divide(self,a,b):
        return a/b
    
Cal = Calculator()

print(Cal.add(10,20))
print(Cal.subtract(10,20))
print(Cal.multiply(10,20))
print(Cal.divide(10,20))


